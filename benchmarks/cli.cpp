// pccl_cli encode  in.ply   out.pccl  [--bits=N] [--preset=Fast|Balanced|Small|Smallest]
// pccl_cli decode  in.pccl  out.ply       (writes minimal binary x/y/z PLY)
// pccl_cli info    in.pccl

#include "pcc_loader.hpp"

#include <pccl/pccl.hpp>

#include <charconv>
#include <chrono>
#include <cstdio>
#include <cstdlib>
#include <cstring>
#include <filesystem>
#include <fstream>
#include <string>
#include <string_view>

namespace
{

const char* preset_name(pccl::Preset p)
{
  switch(p)
  {
    case pccl::Preset::Fast:
      return "Fast";
    case pccl::Preset::Balanced:
      return "Balanced";
    case pccl::Preset::Small:
      return "Small";
    case pccl::Preset::Smallest:
      return "Smallest";
  }
  return "?";
}

bool parse_preset(std::string_view s, pccl::Preset& out)
{
  if(s == "Fast" || s == "fast")
  {
    out = pccl::Preset::Fast;
    return true;
  }
  if(s == "Balanced" || s == "balanced")
  {
    out = pccl::Preset::Balanced;
    return true;
  }
  if(s == "Small" || s == "small")
  {
    out = pccl::Preset::Small;
    return true;
  }
  if(s == "Smallest" || s == "smallest")
  {
    out = pccl::Preset::Smallest;
    return true;
  }
  return false;
}

std::string_view get_flag(int argc, char** argv, std::string_view key)
{
  const std::string prefix = std::string("--") + std::string(key) + "=";
  for(int i = 0; i < argc; ++i)
  {
    std::string_view a = argv[i];
    if(a.starts_with(prefix))
      return a.substr(prefix.size());
  }
  return {};
}

void write_ply_xyz(const std::filesystem::path& path, const pccl::AlignedVec<float>& xyz)
{
  const std::size_t n = xyz.size() / 3;
  std::ofstream f(path, std::ios::binary | std::ios::trunc);
  if(!f)
    throw std::runtime_error("cannot open for write: " + path.string());

  f << "ply\n"
    << "format binary_little_endian 1.0\n"
    << "comment generated by pccl_cli\n"
    << "element vertex " << n << "\n"
    << "property float x\n"
    << "property float y\n"
    << "property float z\n"
    << "end_header\n";
  f.write(
      reinterpret_cast<const char*>(xyz.data()),
      static_cast<std::streamsize>(xyz.size() * sizeof(float)));
  if(!f)
    throw std::runtime_error("write failed: " + path.string());
}

int cmd_encode(int argc, char** argv)
{
  if(argc < 2)
  {
    std::fprintf(
        stderr,
        "usage: pccl_cli encode <in.ply> <out.pccl> "
        "[--bits=N] [--preset=Fast|Balanced|Small|Smallest]\n");
    return 1;
  }
  const std::filesystem::path in_ply = argv[0];
  const std::filesystem::path out_pcc = argv[1];

  pccl::EncodeOptions opts;
  opts.preset = pccl::Preset::Balanced;
  opts.bits = 16;

  if(auto v = get_flag(argc, argv, "bits"); !v.empty())
  {
    unsigned b = 0;
    auto r = std::from_chars(v.data(), v.data() + v.size(), b);
    if(r.ec != std::errc{} || b < 1 || b > 21)
    {
      std::fprintf(stderr, "bad --bits=%.*s (need 1..21)\n", (int)v.size(), v.data());
      return 1;
    }
    opts.bits = (std::uint8_t)b;
  }
  if(auto v = get_flag(argc, argv, "preset"); !v.empty())
  {
    if(!parse_preset(v, opts.preset))
    {
      std::fprintf(stderr, "bad --preset=%.*s\n", (int)v.size(), v.data());
      return 1;
    }
  }

  std::printf("loading %s ...\n", in_ply.c_str());
  auto pc = pcc::load_ply_positions(in_ply.string());
  std::printf("loaded %zu points\n", pc.num_points());

  pccl::Encoder enc;
  pccl::ByteBuffer bytes;
  pccl::EncodeStats st;
  enc.encode(
      std::span<const float>(pc.xyz.data(), pc.num_points() * 3), opts, bytes, &st);

  pccl::write_pccl_file(
      out_pcc, std::span<const std::uint8_t>(bytes.data(), bytes.size()));

  std::printf(
      "encoded %s  preset=%s bits=%u\n"
      "  voxels=%zu  bytes=%zu  ratio=%.4f  B/pt=%.3f  enc=%.1f ms\n",
      out_pcc.c_str(), preset_name(opts.preset), opts.bits, st.unique_voxels,
      st.output_bytes, st.ratio(), st.bytes_per_point(), st.encode_ms);
  return 0;
}

int cmd_decode(int argc, char** argv)
{
  if(argc < 2)
  {
    std::fprintf(stderr, "usage: pccl_cli decode <in.pccl> <out.ply>\n");
    return 1;
  }
  const std::filesystem::path in_pcc = argv[0];
  const std::filesystem::path out_ply = argv[1];

  pccl::ByteBuffer bytes;
  pccl::read_pccl_file(in_pcc, bytes);

  pccl::Decoder dec;
  pccl::AlignedVec<float> xyz;
  pccl::FrameMetadata meta;

  using clk = std::chrono::high_resolution_clock;
  const auto t0 = clk::now();
  dec.decode(std::span<const std::uint8_t>(bytes.data(), bytes.size()), xyz, &meta);
  const auto t1 = clk::now();
  const double dec_ms = std::chrono::duration<double, std::milli>(t1 - t0).count();

  write_ply_xyz(out_ply, xyz);

  std::printf(
      "decoded %s  preset=%s bits=%u  voxels=%u  dec=%.1f ms  -> %s\n", in_pcc.c_str(),
      preset_name(meta.preset), meta.bits, meta.unique_voxels, dec_ms, out_ply.c_str());
  return 0;
}

int cmd_info(int argc, char** argv)
{
  if(argc < 1)
  {
    std::fprintf(stderr, "usage: pccl_cli info <in.pccl>\n");
    return 1;
  }
  const std::filesystem::path in_pcc = argv[0];

  pccl::ByteBuffer bytes;
  pccl::read_pccl_file(in_pcc, bytes);

  pccl::FrameHeader h;
  if(!pccl::peek_header(std::span<const std::uint8_t>(bytes.data(), bytes.size()), h))
  {
    std::fprintf(stderr, "pccl: cannot parse header\n");
    return 1;
  }

  std::printf("file:          %s\n", in_pcc.c_str());
  std::printf("size:          %zu bytes\n", bytes.size());
  std::printf("preset:        %s (%u)\n", preset_name((pccl::Preset)h.preset), h.preset);
  std::printf("bits per axis: %u\n", h.bits);
  std::printf("input points:  %u\n", h.input_points);
  std::printf("unique voxels: %u\n", h.unique_voxels);
  std::printf(
      "aabb min:      %.6g  %.6g  %.6g\n", h.aabb_min[0], h.aabb_min[1], h.aabb_min[2]);
  std::printf(
      "aabb max:      %.6g  %.6g  %.6g\n", h.aabb_max[0], h.aabb_max[1], h.aabb_max[2]);
  std::printf("payload size:  %u\n", h.payload_size);
  if(h.input_points > 0)
  {
    const double raw_bytes = double(h.input_points) * 12.0;
    std::printf(
        "ratio:         %.4f  (input %zu B -> file %zu B)\n", bytes.size() / raw_bytes,
        (std::size_t)(h.input_points * 12), bytes.size());
  }
  return 0;
}

} // namespace

int main(int argc, char** argv)
{
  if(argc < 2)
  {
    std::fprintf(
        stderr,
        "usage: pccl_cli <command> [args...]\n"
        "commands:\n"
        "  encode <in.ply>  <out.pccl> [--bits=N] [--preset=...]\n"
        "  decode <in.pccl> <out.ply>\n"
        "  info   <in.pccl>\n");
    return 1;
  }

  try
  {
    const std::string_view cmd = argv[1];
    if(cmd == "encode")
      return cmd_encode(argc - 2, argv + 2);
    if(cmd == "decode")
      return cmd_decode(argc - 2, argv + 2);
    if(cmd == "info")
      return cmd_info(argc - 2, argv + 2);
    std::fprintf(stderr, "unknown command: %s\n", argv[1]);
    return 1;
  }
  catch(const std::exception& e)
  {
    std::fprintf(stderr, "error: %s\n", e.what());
    return 1;
  }
}
